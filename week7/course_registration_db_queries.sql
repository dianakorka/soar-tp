create table student (
    student_id int generated always as identity not null primary key,
    student_first_name varchar(50),
    student_last_name varchar(50)
);

create table student_takes_course (
	stc_id int generated always as identity not null primary key,
    s_id int,
    c_id int,
    foreign key(s_id) references student(student_id) ON DELETE CASCADE,
    foreign key(c_id) references course(course_id) ON DELETE CASCADE
);

